    public void agregar(String nombre, double precio, int stock) {
        Producto p = new Producto();
        p.isSeleccionado();
        p.setNombre(nombre);
        p.setPrecio(precio);
        p.setStock(stock);
        controlP.agregarProducto(p);
    }

