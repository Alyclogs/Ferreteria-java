    private void txtLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtLogInActionPerformed
        ControlUsuario control = new ControlUsuario();
        String[] User = control.leerUsuario();

        String userIngresado = txtUsername.getText();
        String passIngresada = txtPassword.getText();

        if (!userIngresado.equals("") && !passIngresada.equals("")) {
            if (userIngresado.equals(User[0]) && passIngresada.equals(User[1])) {
                GestionDeInv principal = GestionDeInv.getObj();
                principal.obtenerUsuario(userIngresado);
                principal.setVisible(true);
                this.setVisible(false);
                return;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ambos campos son obligatorios!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        JOptionPane.showMessageDialog(null, "Usuario o contraseña incorrectos", "Vuelva a intentarlo", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_txtLogInActionPerformed

